const LEVELS = [LEVEL_1, LEVEL_2, LEVEL_3];

export const BRICK_COLORS = [
  null,
  '#e87c2e', // level 1 orange
  '#4a90d9', // level 2 blue
  '#9b59b6', // level 3 purple
];

export function getLevel(num) {
  const idx = ((num - 1) % LEVELS.length);
  return LEVELS[idx];
}

export function getLevelColor(num) {
  return BRICK_COLORS[((num - 1) % (BRICK_COLORS.length - 1)) + 1];
}

export function isSolid(map, col, row) {
  if (row < 0 || row >= ROWS) return true;
  const c = ((col % COLS) + COLS) % COLS;
  return map[row][c] === 1;
}

// Returns pixel y of top of tile at given row
export function tileTop(row) { return row * TILE; }

// Get tile coords from pixel
export function pixelToTile(px, py) {
  return { col: Math.floor(px / TILE), row: Math.floor(py / TILE) };
}
